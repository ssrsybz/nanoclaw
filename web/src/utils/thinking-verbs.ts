// 缓存上次使用的动词，避免连续重复
let lastVerbIndex = -1;

/**
 * 获取随机思考动词
 */
export function getRandomThinkingVerb(): string {
  let index: number;

  // 避免连续两次选择相同的动词
  do {
    index = Math.floor(Math.random() * THINKING_VERBS.length);
  } while (index === lastVerbIndex && THINKING_VERBS.length > 1);

  lastVerbIndex = index;
  return THINKING_VERBS[index];
}

/**
 * 获取所有思考动词
 */
export function getAllThinkingVerbs(): string[] {
  return [...THINKING_VERBS];
}

/**
 * 添加自定义思考动词
 */
export function addThinkingVerbs(verbs: string[]): void {
  THINKING_VERBS.push(...verbs);
}
